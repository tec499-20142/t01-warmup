   private void printar(String a, String b, String r) {
      jPanel2.paint(jPanel2.getGraphics());
      Graphics g = jPanel2.getGraphics();
      Color color;
      int diametro = 10;
      String vb;
      for (int x = 0; x < a.length(); x++) {
         if (a.charAt(x) == '1') {
            color = Color.red;
            vb = "1";
         } else {
            color = Color.GRAY;
            vb = "0";
         }
         ((Graphics2D) g).setColor(color);
         ((Graphics2D) g).setStroke(new BasicStroke(2));
         ((Graphics2D) g).fillOval(20 * x, 6, diametro, diametro);
         ((Graphics2D) g).setColor(Color.black);
         ((Graphics2D) g).drawOval(20 * x, 6, diametro, diametro);
         ((Graphics2D) g).drawString(vb, 21 * x, 27);
      }
      for (int x = 0; x < b.length(); x++) {
         if (b.charAt(x) == '1') {
            color = Color.red;
            vb = "1";
         } else {
            color = Color.GRAY;
            vb = "0";
         }
         ((Graphics2D) g).setColor(color);
         ((Graphics2D) g).setStroke(new BasicStroke(2));
         ((Graphics2D) g).fillOval(20 * x, 33, diametro, diametro);
         ((Graphics2D) g).setColor(Color.black);
         ((Graphics2D) g).drawOval(20 * x, 33, diametro, diametro);
         ((Graphics2D) g).drawString(vb, 21 * x, 54);
      }
      for (int x = 0; x < r.length(); x++) {
         Color colorb = Color.BLACK;
         if (r.charAt(x) == '1') {
            color = Color.red;
            vb = "1";
            if (x == 0 && r.length() >= 9) {
               color = Color.YELLOW;
               colorb = Color.RED;
            }
         } else {
            color = Color.GRAY;
            vb = "0";
         }
         ((Graphics2D) g).setColor(color);
         ((Graphics2D) g).setStroke(new BasicStroke(2));
         ((Graphics2D) g).fillOval(20 * x, 62, diametro, diametro);
         ((Graphics2D) g).setColor(colorb);
         ((Graphics2D) g).drawOval(20 * x, 62, diametro, diametro);
         ((Graphics2D) g).drawString(vb, 21 * x, 83);
      }
   }

