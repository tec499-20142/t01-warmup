   private void listabintela() {

      String text = jTextField1.getText().trim();
      String[] ops;
      int resultado = 0;
      if (text.indexOf("+") != -1) { //Se for diferente de -1 é pq existe.
         ops = text.split(java.util.regex.Pattern.quote("+"));
         if (ops.length == 2 && !ops[0].equals("")) {
            resultado = this.somar(Integer.parseInt(ops[0]), Integer.parseInt(ops[1]));
            this.comunica(ops[0], ops[1], "+");
         }
      } else if (text.indexOf("-") != -1) {
         ops = text.split(java.util.regex.Pattern.quote("-"));
         if (ops.length == 2 && !ops[0].equals("")) {
            resultado = this.diminuir(Integer.parseInt(ops[0]), Integer.parseInt(ops[1]));
            this.comunica(ops[0], ops[1], "+");
         }
      } else if (text.indexOf("*") != -1) {
         ops = text.split(java.util.regex.Pattern.quote("*"));
         if (ops.length == 2 && !ops[0].equals("")) {
            resultado = this.multiplicar(Integer.parseInt(ops[0]), Integer.parseInt(ops[1]));
            this.comunica(ops[0], ops[1], "*");
         }
      } else if (text.indexOf("/") != -1) {
         ops = text.split(java.util.regex.Pattern.quote("/"));
         if (ops.length == 2 && !ops[0].equals("")) {
            if (Integer.parseInt(ops[1]) == 0) {
               javax.swing.JOptionPane.showMessageDialog(null, "Não se pode efetuar divisão por zero!", "Atenção!", 2);
               return;
            }
            resultado = this.dividir(Integer.parseInt(ops[0]), Integer.parseInt(ops[1]));
            this.comunica(ops[0], ops[1], "/");
         }
      } else if (text.indexOf("&") != -1) {
         ops = text.split(java.util.regex.Pattern.quote("&"));
         if (ops.length == 2 && !ops[0].equals("")) {
            resultado = this.and(Integer.parseInt(ops[0]), Integer.parseInt(ops[1]));
            this.comunica(ops[0], ops[1], "&");
         }
      } else if (text.indexOf("|") != -1) {
         ops = text.split(java.util.regex.Pattern.quote("|"));
         if (ops.length == 2 && !ops[0].equals("")) {
            resultado = this.or(Integer.parseInt(ops[0]), Integer.parseInt(ops[1]));
            this.comunica(ops[0], ops[1], "|");
         }
      } else {
         javax.swing.JOptionPane.showMessageDialog(null, "Escolha ou Digite o Segundo Operando!", "Atenção!", 2);
         return;
      }
      if (resultado > 511) {
         /**
          * *************VERIFICAR DEPOIS***************
          */
      }
      if (ops.length > 2) {
         javax.swing.JOptionPane.showMessageDialog(null, "Retifique! Só poderá efetuar uma operação", "Atenção!", 2);
         return;
      }
      if (ops.length == 1) {
         javax.swing.JOptionPane.showMessageDialog(null, "Digite o próximo Operando!", "Atenção!", 2);
         return;
      }
      if (ops.length == 0 || ops[0].equals("")) {
         javax.swing.JOptionPane.showMessageDialog(null, "Verifique sua operação!", "Atenção!", 2);
         return;
      }
      String bin1 = Integer.toString(Integer.parseInt(ops[0]), 2);
      String bin2 = Integer.toString(Integer.parseInt(ops[1]), 2);
      String result = Integer.toString(resultado, 2);
      if (bin1.length() > 8 || bin2.length() > 8) {
         javax.swing.JOptionPane.showMessageDialog(null, "Retifique! O Operando deve estar entre 0 e 255!", "Atenção!", 2);
         return;
      }
      //preencher operadorandos e resultado
      jLabel5.setText(ops[0]);
      jLabel6.setText(ops[1]);
      if (resultado > 255) {
         jLabel7.setText("Err");
      } else {
         jLabel7.setText(Integer.toString(resultado));
      }

      String B1 = this.preenchebinario(bin1);
      String B2 = this.preenchebinario(bin2);
      String B3 = this.preenchebinario(result);

      this.printar(B1, B2, B3);

   }

   private String preenchebinario(String bin) {
      StringBuffer sb = new StringBuffer(bin);
      int j = bin.length();
      for (int i = 0; i < 8 - j; i++) {
         sb.insert(0, '0');
      }
      if (bin.length() > 9) {
         StringBuffer sbmodif = null;
         for (int k = 0; sb.length() != 9; k++) {
            sbmodif = sb.deleteCharAt(k);
         }
         return String.valueOf(sbmodif);
      }
      return String.valueOf(sb);
   }

